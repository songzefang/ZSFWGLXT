package com.jcz.entity;

public class Visitor {

	private Integer id;
	private String visPhone;
	private String visPwd;
	private String visName;
	private String visSex;
	private String company;
	private String job;
	private String location;
	private String email;
	private Integer state;
	
	public Integer getId() {
		return id;
	}
	public void setId(Integer id) {
		this.id = id;
	}
	public String getVisPhone() {
		return visPhone;
	}
	public void setVisPhone(String visPhone) {
		this.visPhone = visPhone;
	}
	public String getVisPwd() {
		return visPwd;
	}
	public void setVisPwd(String visPwd) {
		this.visPwd = visPwd;
	}
	public String getVisName() {
		return visName;
	}
	public void setVisName(String visName) {
		this.visName = visName;
	}
	
	public String getVisSex() {
		return visSex;
	}
	public void setVisSex(String visSex) {
		this.visSex = visSex;
	}
	public String getCompany() {
		return company;
	}
	public void setCompany(String company) {
		this.company = company;
	}
	public String getJob() {
		return job;
	}
	public void setJob(String job) {
		this.job = job;
	}
	public String getLocation() {
		return location;
	}
	public void setLocation(String location) {
		this.location = location;
	}
	public String getEmail() {
		return email;
	}
	public void setEmail(String email) {
		this.email = email;
	}
	public Integer getState() {
		return state;
	}
	public void setState(Integer state) {
		this.state = state;
	}



}
